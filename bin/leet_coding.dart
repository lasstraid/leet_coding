import 'package:leet_coding/leet_coding.dart' as leet_coding;

void main(List<String> arguments) {
  print(
    'Input: nums = [2, 3, 1, 1, 4] Expected: 2 Output: ${leet_coding.jump([2, 3, 1, 1, 4])}',
  );
  print(
    'Input: nums = [2,3,0,1,4] Expected: 2 Output: ${leet_coding.jump([2, 3, 0, 1, 4])}',
  );
  print(
    'Input: nums = [4,1,1,3,1,1,1] Expected: 2 Output: ${leet_coding.jump([4, 1, 1, 3, 1, 1, 1])}',
  );
}
